#include "../string/ft_string.h"
#include "../memory/ft_memory.h"
#include <unistd.h>
#include <stdlib.h>

t_str 	ft_strjoin(t_str s1, t_str s2)
{
	t_str 	str;
	int		i;
	int		j;

	if (!s1 || !s2)
		return (NULL);
	str = ft_safe_malloc(
			sizeof(char) * (ft_strlen(s1) + ft_strlen(s2) + 1));
	if (!str)
		return (NULL);
	i = 0;
	j = 0;
	while (s1[i])
	{
		str[j++] = s1[i];
		i++;
	}
	i = 0;
	while (s2[i])
	{
		str[j++] = s2[i];
		i++;
	}

	return (str);
}
