#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, int n)
{
	int	i;
	char	*d;
	char	*s;

	i = 0;
	d = (char *)dst;
	s = (char *)src;
	while (i < n)
	{
		d[i] = s[i];
		i++;
	}
	return (dst);
}